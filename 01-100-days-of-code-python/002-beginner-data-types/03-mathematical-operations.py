# Sum
print(2 + 3)  # print 5 (int)
print(2.5 + 5)  # print 7.5 (float)

# Subtraction
print(5 - 3)  # print 2 (int)
print(2.5 - 5)  # print -2.5 (float)

# Multiplication
print(5 * 3)  # print 15 (int)
print(2.5 * 5)  # print 12.5 (float)

# Division
print(6 / 3)  # print 2.0 (float)
print(8 / 3)  # print 2.6666666666666665 (float)
print(8 // 3)  # print 2 (int) [convert to integer]

# Exponentiation
print(2**3)  # print 8 (int)

# Importance of mathematical operations (), **, * or /, + or -
print(3 * 3 + 3 / 3 - 3)  # print 7.0 (float)
