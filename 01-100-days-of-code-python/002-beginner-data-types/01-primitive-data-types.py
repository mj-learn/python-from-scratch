# String
# Ordered sequence of characters: "hello", 'Sammy', "2000"
print("Hello"[0])  # print first character of the string

# Integer
# Whole numbers: -1_000_000, -3000, -300, -3, 3, 300, 3000, 1_000_000

print(100_000_789)  # 100,000,789

# Float
# Numbers with a decimal point: 2.3, 45.7, 2000.5
print(3.14159)  # pi

# Boolean
# Logical value indicating True or False
print(True)
print(False)
